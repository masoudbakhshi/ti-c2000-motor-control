//   and debug connection are all confirmed working before any motor-control
//   firmware is loaded.
//
//   Blink pattern  :  500 ms ON  →  500 ms OFF  →  repeat
//   Active logic   :  LED turns ON when GPIO31 is driven LOW (active-low)
//
// HARDWARE:
//   Board   : TMDSCNCD28388D Rev.B controlCARD
//   Dock    : TMDSHSECDOCK
//   LED D1  : GPIO31  (HSEC connector, pin 82)
//   LED D2  : GPIO34  (HSEC connector — reserved, not driven here)
