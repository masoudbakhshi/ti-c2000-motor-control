#include "driverlib.h"
#include "device.h"
#include "board.h"
#include "c2000ware_libraries.h"

void main(void)
{
    //
    // Initialize the device clock tree and all peripheral power domains.
    // This configures the PLL, sets up the CPU frequency, and brings the
    // oscillator to a stable operating point before any peripheral is touched.
    //
    Device_init();

    //
    // Release the GPIO multiplexer from its reset-state lockout and configure
    // the pull-up network.  Must be called before any GPIO register is written.
    //
    Device_initGPIO();

    //
    // Initialize the Peripheral Interrupt Expansion (PIE) module and clear
    // all interrupt flag registers.  This also disables CPU-level interrupts,
    // giving the initialization sequence a clean, deterministic environment.
    //
    Interrupt_initModule();

    //
    // Load the PIE vector table with the default shell ISR addresses supplied
    // by the driverlib startup code.  Any vector not later overridden will
    // safely land in the shell handler.
    //
    Interrupt_initVectorTable();

    //
    // Apply the pin-mux and GPIO direction settings generated by SysConfig.
    // After this call, GPIO31 is configured as a push-pull output and its
    // initial state is set high (LED off).
    //
    Board_init();

    //
    // Initialize any C2000Ware library components included via SysConfig.
    //
    C2000Ware_libraries_init();

    //
    // Unmask the global interrupt flag (INTM) and the debug-override interrupt
    // mask (DBGM) so the CPU can service real-time interrupts through the PIE.
    //
    EINT;
    ERTM;

    //
    // Main loop — blink LED D1 at 1 Hz indefinitely.
    //
    for(;;)
    {
        //
        // Drive GPIO31 low: turns LED D1 on (active-low circuit).
        //
        GPIO_writePin(myBoardLED0_GPIO, 0);
        DEVICE_DELAY_US(500000UL);

        //
        // Drive GPIO31 high: turns LED D1 off.
        //
        GPIO_writePin(myBoardLED0_GPIO, 1);
        DEVICE_DELAY_US(500000UL);
    }
}

